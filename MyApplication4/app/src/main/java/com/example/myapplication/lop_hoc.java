package com.example.myapplication;

public class lop_hoc {
    String lop,khoa,nam;

    public lop_hoc(String lop, String khoa, String nam) {
        this.lop = lop;
        this.khoa = khoa;
        this.nam = nam;
    }

    public String getLop() {
        return lop;
    }

    public String getKhoa() {
        return khoa;
    }

    public String getNam() {
        return nam;
    }
}
